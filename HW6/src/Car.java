public class Car {
    int id;
    int prodYear;
    int price;
    int regNo;
    String brand;
    String model;
    String colour;

    public Car (int id, int prodYear, int price, int regNo, String brand, String model, String colour) {
        this.id = id;
        this.prodYear = prodYear;
        this.price = price;
        this.regNo = regNo;
        this.brand = brand;
        this.model = model;
        this.colour = colour;
    }
}